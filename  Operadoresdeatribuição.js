/*  Operadores de atribuição

= -> atribuição
+= -> adição
-= -> subtração
/= -> divisao
*= -> multiplicação
%= -> resto

Exemplos abaixo de operador de atribuição, bom pra encurtar code
*/

let n1 = 10
n1 += 5
console.log(n1)

let n2 = 20
n2 -= 5
console.log(n2);

let n3 = 25
n3 /= 5
console.log(n3);

let n4 = 50
n4 *= 2
console.log(n4);

let n5 = 30
n5 %= 2
console.log(n5);

// realizando calculo com operadores de atribuição
