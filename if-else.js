/*

if = Se
else = Se não


Na programação, a instrução if...else é uma instrução de fluxo de controle que permite executar um determinado bloco de código
se uma determinada condição for atendida e um bloco de código diferente se a condição não for atendida. 
A parte if da instrução especifica a condição que deve ser atendida e a parte else especifica
 o código que será executado se a condição não for atendida.


let x = 5;

if (x > 10) {
  console.log("x is greater than 10");
} else {
  console.log("x is not greater than 10");
}

Neste exemplo, a instrução if verifica se o valor de x é maior que 10. 
Como o valor de x não é maior que 10, o bloco de código else é executado, 
o que gera a mensagem "x não é maior que 10 " para a consola.

*/



